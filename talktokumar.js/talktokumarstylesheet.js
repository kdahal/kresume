function submitForme(){
	_("myform").disabled = true;
	_("stode").innerHTML = ' please wait ...';
	var formdata = new FormData();
	formdata.append( "name1", _("name1").value );
	formdata.append( "email2", _("email2").value );
	formdata.append( "comment3", _("comment3").value );
	var ajax = new XMLHttpRequest();
	ajax.open( "POST", "contact12.php" );
	ajax.onreadystatechange = function() {
		if(ajax.readyState == 4 && ajax.status == 200) {
			if(ajax.responseText == "success"){
				_("form20").innerHTML = '<h4> Thanks '+_("name1").value+', your message was successfully sent!</h4>';
			} else {
				_("stode").innerHTML = ajax.responseText;
				_("myform").disabled = false;
			}
		}
	}
	ajax.send( formdata );
}



